package sundar;
import java.util.Scanner;
public class pattern113 {
	public static void main(String[] args) {
		Scanner a = new Scanner(System.in);
		System.out.println("enter a no.:");
		int n = a.nextInt();
		
		System.out.println("enter a no.:");
		int m = a.nextInt();
	
		System.out.println(--n);
		System.out.println(n<m);
		System.out.println(n>m);
		System.out.println(n==m);
		System.out.println(n<=m);
		System.out.println(n>=m);
		System.out.println(n!=m);
		
	}

}
