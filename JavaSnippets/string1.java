package sri;

public class string1 {
	public static void main(String[] args) {
		String word="hello\"mosh\"";
		System.out.print(word);
		System.out.println();
		String word1="hello\\mosh\\";
		System.out.print(word1);
		System.out.println();
		String word2="hello\tmosh\\";
		System.out.print(word2);
		System.out.println();
		String word3="hello\nmosh\\";
		System.out.print(word3);
	}

}
